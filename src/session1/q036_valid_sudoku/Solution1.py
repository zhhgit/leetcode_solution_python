class Solution1:
    def isValidSudoku(self, board):
        # 验证行
        for i in range(0,9):
            if not self.valid9Nums(board[i]):
                return False
        # 验证列
        for i in range(0,9):
            arr = []
            for j in range(0,9):
                arr.append(board[j][i])
            if not self.valid9Nums(arr):
                return False
        # 验证3*3
        for i in range(0,3):
            for j in range(0,3):
                arr = []
                for k in range(i * 3,i * 3 + 3):
                    for l in range(j * 3, j * 3 + 3):
                        arr.append(board[k][l])
                if not self.valid9Nums(arr):
                    return False
        return True

    def valid9Nums(self,arr):
        dict = {}
        for i in range(0,len(arr)):
            if arr[i] != ".":
                if arr[i] not in dict:
                    dict[arr[i]] = 1
                else:
                    return False
        return True

